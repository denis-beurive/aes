static uint8_t gmul_inverse(const uint8_t in) {
    /* 0 is self inverting */
    if (0 == in) return 0;
    return atable[(255 - ltable[in])];
}

/**
 * Calculate the forward s-box for a given number.
 *
 * Please note that this function is used to generate the forward s-box lookup table.
 *
 * @param in The given number.
 * @return The forward s-box for a given number.
 * @see Rijndael's S-box (https://www.samiam.org/s-box.html).
 * @see Forward S-box (https://en.wikipedia.org/wiki/Rijndael_S-box)
 */
uint8_t aes_calculate_forward_sbox(const uint8_t in) {
    uint8_t s, x;
    s = x = gmul_inverse(in);
    for(int c = 0; c < 4; c++) {
        s = (s << 1) | (s >> 7);  // one bit circular rotate to the left
        x ^= s; // xor with x
    }
    x ^= 99; /* 0x63 */
    return x;
}

/**
 * Calculate the inverse s-box for a given number.
 *
 * Please note that this function is used to generate the inverse s-box lookup table.
 *
 * @param in The given number.
 * @return The inverse s-box for a given number.
 * @see Rijndael's S-box (https://www.samiam.org/s-box.html).
 * @see Forward S-box (https://en.wikipedia.org/wiki/Rijndael_S-box)
 */

uint8_t aes_calculate_reverse_sbox(const uint8_t in) {

    // calculating the inverse affine transformation of the input value

    uint8_t v = ROTL8(in,1) ^ ROTL8(in,3) ^ ROTL8(in,6) ^ 0x05;
    return gmul_inverse(v);
}

/**
 * Perform the MixColumns operation on a single column.
 *
 * @param column A column of a state.
 * @see Rijndael's mix column stage (https://en.wikipedia.org/wiki/Rijndael_MixColumns)
 * @note This function is not used for implementing the AES algorithm.
 */

void aes_mix_single_column(uint8_t column[4]) {
    uint8_t a[4];
    uint8_t b[4];

    for (int c = 0; c < 4; c++) {
        // `h` is set to `0x01` if the high bit of `column[c]` is set, `0x00` otherwise.
        const uint8_t h = column[c] >> 7;
        // the array `a` is simply a copy of the input array `column`
        a[c] = column[c];
        // the array `b` is each element of the array `a` multiplied by 2 in Rijndael's Galois field
        b[c] = (column[c] << 1) ^ (h * 0x1B);
    }
    column[0] = b[0] ^ a[3] ^ a[2] ^ b[1] ^ a[1]; /* 2 * a0 + a3 + a2 + 3 * a1 */
    column[1] = b[1] ^ a[0] ^ a[3] ^ b[2] ^ a[2]; /* 2 * a1 + a0 + a3 + 3 * a2 */
    column[2] = b[2] ^ a[1] ^ a[0] ^ b[3] ^ a[3]; /* 2 * a2 + a1 + a0 + 3 * a3 */
    column[3] = b[3] ^ a[2] ^ a[1] ^ b[0] ^ a[0]; /* 2 * a3 + a2 + a1 + 3 * a0 */
}

